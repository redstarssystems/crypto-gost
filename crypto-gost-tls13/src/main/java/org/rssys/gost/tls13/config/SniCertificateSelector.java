package org.rssys.gost.tls13.config;

/**
 * Функциональный интерфейс для выбора сертификата по SNI (Server Name Indication).
 * <p>
 * Вызывается сервером при получении {@code server_name} расширения в ClientHello.
 * Позволяет подменять учётные данные (цепочку сертификатов, ключ, OCSP) в
 * зависимости от запрошенного имени хоста. Если селектор возвращает null,
 * сервер продолжает handshake с учётными данными из первоначальной конфигурации.
 * <p>
 * Предназначен в первую очередь для будущего JSSE-модуля ({@code GostSSLEngine}),
 * который будет использовать этот интерфейс для выбора сертификата в multi-tenant
 * развёртываниях (например, в корпоративных шлюзах).
 * <p>
 * Реализация MUST быть:
 * <ul>
 *   <li><b>thread-safe</b> — может вызываться конкурентно из разных handshake-потоков</li>
 *   <li><b>быстрой</b> — вызывается синхронно во время handshake</li>
 *   <li><b>неблокирующей</b> — без I/O, сетевых вызовов или ожидания блокировок</li>
 * </ul>
 */
@FunctionalInterface
public interface SniCertificateSelector {

    /**
     * Выбирает учётные данные сервера по имени хоста из SNI.
     *
     * @param serverName запрошенное имя хоста (нормализованное: lowercase, без точки на конце)
     * @return учётные данные для этого имени, или null для использования конфигурации по умолчанию
     */
    TlsServerCredentials select(String serverName);
}
